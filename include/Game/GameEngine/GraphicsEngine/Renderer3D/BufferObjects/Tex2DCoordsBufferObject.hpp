#ifndef TEX2DCOORDSBUFFEROBJECT_HPP__
#define TEX2DCOORDSBUFFEROBJECT_HPP__

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////
#include <Game/includes.hpp>
#include <Game/GameEngine/GraphicsEngine/Renderer3D/BufferObjects/Tex2DCoordsArray.hpp>

////////////////////////////////////////////////////////////
/// \brief Class to define the texture 2D coordinates of the VBOs.
///
////////////////////////////////////////////////////////////
class Tex2DCoordsBufferObject {

  protected :
    ////////////////////////////////////////////////////////////
    // Member data
    ////////////////////////////////////////////////////////////
    Tex2DCoordsArray  m_oTex2DCoords;

  public:
    ////////////////////////////////////////////////////////////
    // Constructor(s)/Destructor
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    /// \brief Default constructor.
    ///
    /// This constructor defines a VBO.
    ///
    ////////////////////////////////////////////////////////////
    Tex2DCoordsBufferObject          ( void );

    ////////////////////////////////////////////////////////////
    /// \brief Destructor.
    ///
    /// Cleans up all the internal resources used by the VBO.
    ///
    ////////////////////////////////////////////////////////////
    virtual ~Tex2DCoordsBufferObject ( void );

    ////////////////////////////////////////////////////////////
    // Internal methods
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    /// \brief Active the texture 2D coordinates pointer of OpenGL.
    ///
    /// \return True if activation succeeded, false if it failed.
    ///
    ////////////////////////////////////////////////////////////
    GLboolean ActiveTex2DCoordsPointer ( void );
};

#endif // TEX2DCOORDSBUFFEROBJECT_HPP__
