#ifndef GAMEOBJECT2D_HPP__
#define GAMEOBJECT2D_HPP__

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////
#include <Game/includes.hpp>

////////////////////////////////////////////////////////////
/// \brief Class to create 2D game objects like as title, texts, etc...
///
////////////////////////////////////////////////////////////
class GameObject2D {

  protected :
    ////////////////////////////////////////////////////////////
    // Member data
    ////////////////////////////////////////////////////////////
    GLuint    m_uiRenderList2D_ID;

  public :
    ////////////////////////////////////////////////////////////
    // Constructor(s)/Destructor
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    /// \brief Default constructor.
    ///
    /// This constructor defines a 2D game object.
    ///
    ////////////////////////////////////////////////////////////
    GameObject2D ( void );

    ////////////////////////////////////////////////////////////
    /// \brief Destructor.
    ///
    /// Cleans up all the internal resources used by the 2D game object.
    ///
    ////////////////////////////////////////////////////////////
    virtual ~GameObject2D ( void );

    ////////////////////////////////////////////////////////////
    // Accessor methods
    ////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////
    /// \brief Set the 2D render list identifier to insert the 2D game object.
    ///
    /// \param uiRenderList2D_ID  Identifier of the render list.
    ///
    ////////////////////////////////////////////////////////////
    void SetRenderList2D_ID ( GLuint uiRenderList2D_ID );
};

#endif // GAMEOBJECT2D_HPP__
